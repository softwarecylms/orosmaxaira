// Display order on the hub (by slug).
const RANK: Record<string, number> = {
  melissolampades: 0,
  "peritiligma-fagitou": 1,
  "kerines-dimiourgies": 2,
  "fytefsi-sporon": 3,
  "ergastiria-mageirikis": 4,
  keraloifes: 5,
}

export default async function seedWorkshops({ container }: ExecArgs) {
  const logger = container.resolve("logger")
  const bookings = container.resolve(BOOKINGS_MODULE) as BookingsModuleService

  for (const w of WORKSHOPS) {
    const data = {
      ...w,
      rank: RANK[w.slug] ?? 99,
      duration_label: "45 λεπτά",
      age_label: "Για όλες τις ηλικίες",
      currency: "eur",
      status: "published",
      // Booking = experience combination, each a priced tier (DEMO prices).
      price_tiers: DEMO_COMBOS,
    }
    const existing = await bookings.listWorkshops({ slug: w.slug })
    if (existing.length) {
      await bookings.updateWorkshops({ id: existing[0].id, ...data } as any)
      logger.info(`Updated workshop ${w.slug}`)
    } else {
      await bookings.createWorkshops(data as any)
      logger.info(`Created workshop ${w.slug}`)
    }
  }
  logger.info("Workshop seed complete.")
}
