// Generate src/components/awards/awards-manifest.ts — each award slug's optimized
// webp paths plus intrinsic width/height (so the masonry gallery + next/image keep
// correct aspect ratios). Re-run after changing award photos:
//   node scripts/awards-manifest.mjs
import sharp from 'sharp'
import { readdirSync, writeFileSync, statSync } from 'node:fs'
import { join } from 'node:path'

const ROOT = 'public/images/awards'
const slugs = readdirSync(ROOT, { withFileTypes: true })
  .filter((d) => d.isDirectory() && d.name !== '_raw')
  .map((d) => d.name)
  .sort()

const out = {}
for (const slug of slugs) {
  const files = readdirSync(join(ROOT, slug)).filter((f) => f.endsWith('.webp')).sort()
  out[slug] = []
  for (const f of files) {
    const fp = join(ROOT, slug, f)
    const m = await sharp(fp).metadata()
    // ?v=<size> busts next/image's cache when a photo is replaced in place
    const v = statSync(fp).size
    out[slug].push({ src: `/images/awards/${slug}/${f}?v=${v}`, w: m.width, h: m.height })
  }
}

const ts = `// AUTO-GENERATED by scripts/awards-manifest.mjs — do not hand-edit.
// Award image paths + intrinsic dimensions (for masonry aspect ratios).
export type AwardImage = { src: string; w: number; h: number }
export const AWARD_IMAGES: Record<string, AwardImage[]> = ${JSON.stringify(out, null, 2)}
`
writeFileSync('src/components/awards/awards-manifest.ts', ts)
console.log('wrote src/components/awards/awards-manifest.ts')
for (const s of slugs) console.log(`  ${s}: ${out[s].length}`)
